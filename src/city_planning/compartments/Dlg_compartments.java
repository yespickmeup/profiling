    public static ArrayListModel tbl_compartments_ALM;
    public static TblcompartmentsModel tbl_compartments_M;

    public static void init_tbl_compartments(JTable tbl_compartments) {
        tbl_compartments_ALM = new ArrayListModel();
        tbl_compartments_M = new TblcompartmentsModel(tbl_compartments_ALM);
        tbl_compartments.setModel(tbl_compartments_M);
        tbl_compartments.setSelectionMode(ListSelectionModel.SINGLE_INTERVAL_SELECTION);
        tbl_compartments.setRowHeight(25);
        int[] tbl_widths_compartments = {100, 0};
        for (int i = 0, n = tbl_widths_compartments.length; i < n; i++) {
            if (i == 0) {
                continue;
            }
            TableWidthUtilities.setColumnWidth(tbl_compartments, i, tbl_widths_compartments[i]);
        }
        Dimension d = tbl_compartments.getTableHeader().getPreferredSize();
        d.height = 25;
        tbl_compartments.getTableHeader().setPreferredSize(d);
        tbl_compartments.getTableHeader().setFont(new java.awt.Font("Arial", 0, 12));
        tbl_compartments.setRowHeight(25);
        tbl_compartments.setFont(new java.awt.Font("Arial", 0, 12));
    }

    public static void loadData_compartments(List<to_compartments> acc) {
        tbl_compartments_ALM.clear();
        tbl_compartments_ALM.addAll(acc);
    }

    public static class TblcompartmentsModel extends AbstractTableAdapter {

        public static String[] COLUMNS = {
            "Compartment", "compartment"
        };

        public TblcompartmentsModel(ListModel listmodel) {
            super(listmodel, COLUMNS);
        }

        @Override
        public boolean isCellEditable(int row, int column) {
            if (column == 100) {
                return true;
            }
            return false;
        }

        @Override
        public Class getColumnClass(int col) {
            if (col == 1000) {
                return Boolean.class;
            }
            return Object.class;
        }

        @Override
        public Object getValueAt(int row, int col) {
            to_compartments tt = (to_compartments) getRow(row);
            switch (col) {
                case 0:
                    return " " + tt.compartment;
                default:
                    return tt.compartment;
            }
        }
    }

    private void ret_compartments() {
        String where = "";
        List<to_compartments> datas = Compartments.ret_data(where);
        loadData_compartments(datas);
        jLabel2.setText("" + datas.size());
    }

    private void ok1() {
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData());
        }
    }

    private void add_compartments() {

        int id = 0;
        String compartment = tf_compartment.getText();

        to_compartments to = new to_compartments(id, compartment, false);
        Compartments.add_data(to);
        Alert.set(1, compartment);
        tf_compartment.setText("");
        if (is_callback_triggered == 1) {
            ok1();
        }
    }

    private void select_compartments() {

        int row = tbl_compartments.getSelectedRow();
        if (row < 0) {
            return;
        }
        to_compartments to = (to_compartments) tbl_compartments_ALM.get(row);
        tf_compartment.setText(to.compartment);

    }

    private void update_compartments() {

        int row = tbl_compartments.getSelectedRow();
        if (row < 0) {
            return;
        }
        to_compartments to = (to_compartments) tbl_compartments_ALM.get(row);
        int id = to.id;
        String compartment = tf_compartment.getText();

        to_compartments to1 = new to_compartments(id, compartment, false);
        Compartments.update_data(to1);
        Alert.set(1, compartment);
        tf_compartment.setText("");
        if (is_callback_triggered == 1) {
            ok1();
        }
    }

    private void delete_compartments() {

        int row = tbl_compartments.getSelectedRow();
        if (row < 0) {
            return;
        }
        final to_compartments to = (to_compartments) tbl_compartments_ALM.get(row);
        Window p = (Window) this;
        Dlg_confirm_action nd = Dlg_confirm_action.create(p, true);
        nd.setTitle("");

        nd.setCallback(new Dlg_confirm_action.Callback() {

            @Override
            public void ok(CloseDialog closeDialog, Dlg_confirm_action.OutputData data) {
                closeDialog.ok();
                Compartments.delete_data(to);
                tf_compartment.setText("");
                if (is_callback_triggered == 1) {
                    ok1();
                }
            }
        });
        nd.setLocationRelativeTo(this);
        nd.setVisible(true);

    }

